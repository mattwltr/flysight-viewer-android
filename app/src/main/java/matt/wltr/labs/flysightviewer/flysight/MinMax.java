package matt.wltr.labs.flysightviewer.flysight;

public class MinMax {

    private double min;

    private double max;

    public MinMax() {}

    public MinMax(double min, double max) {
        this.min = min;
        this.max = max;
    }

    public double getMin() {
        return min;
    }

    public double getMax() {
        return max;
    }
}
